public class forLoops6d
{
	public static void main(String[] args)
	{
	// for( iterator; conditional statement; change iterator)
		for(int i=10; i >=1; i--)
		{ System.out.println(i);}
		
		int m, n;
		for(m=1, n=2; m <=9; m+=2, n+=2)
		{ System.out.println(m + "\n" + n); }   // difference 'n' vs. "n" !!!!!
		
	}



}
