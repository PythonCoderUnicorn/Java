public class switch5
{
	public static void main(String[] args)
	{
		char theGrade = 'a';
		
		switch (theGrade)
		{
			case 'a':
			case 'A':
				System.out.println(" Great job");
				break;
			case 'b':
			case 'B':
				System.out.println(" Good job");
				break;
			case 'c':
			case 'C':
				System.out.println(" okay");
				break;
			case 'D':
				System.out.println(" not so great");
				break;
			default:
				System.out.println(" you failed");
				break;
			}
			
	
	
	
	}








}
